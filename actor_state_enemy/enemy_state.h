#pragma once
#include "../actor_state.h"

//--------------------------------------------------------------------------------
//  前方宣言
//--------------------------------------------------------------------------------
class EnemyController;
class Collider;
class CollisionInfo;

//--------------------------------------------------------------------------------
//  エネミーステートの基底クラス
//--------------------------------------------------------------------------------
class EnemyState : public ActorState
{
public:
    //--------------------------------------------------------------------------------
    //  constructors for singleton
    //--------------------------------------------------------------------------------
    EnemyState(const String& name) : ActorState(name) {}
    ~EnemyState() {}

    //--------------------------------------------------------------------------------
    //  初期化処理
    //--------------------------------------------------------------------------------
    virtual void Init(EnemyController& actor) = 0;

    //--------------------------------------------------------------------------------
    //  終了処理
    //--------------------------------------------------------------------------------
    virtual void Uninit(EnemyController& actor) = 0;

    //--------------------------------------------------------------------------------
    //  更新処理
    //--------------------------------------------------------------------------------
    virtual void Update(EnemyController& actor) = 0;

    //--------------------------------------------------------------------------------
    //  後更新処理
    //--------------------------------------------------------------------------------
    virtual void LateUpdate(EnemyController& actor) = 0;

    //--------------------------------------------------------------------------------
    //  コライダートリガーの時呼ばれる
    //--------------------------------------------------------------------------------
    virtual void OnTrigger(EnemyController& actor, Collider& self, Collider& other) = 0;

    //--------------------------------------------------------------------------------
    //  コライダー衝突の時呼ばれる
    //--------------------------------------------------------------------------------
    virtual void OnCollision(EnemyController& actor, CollisionInfo& info) = 0;
};

//--------------------------------------------------------------------------------
//  ヌルエネミーステート
//--------------------------------------------------------------------------------
class NullEnemyState : public EnemyState
{
public:
    //--------------------------------------------------------------------------------
    //  constructors for singleton
    //--------------------------------------------------------------------------------
    NullEnemyState() : EnemyState(L"NullEnemyState") {}
    ~NullEnemyState() {}

    //--------------------------------------------------------------------------------
    //  初期化処理
    //--------------------------------------------------------------------------------
    void Init(EnemyController& actor) override {}

    //--------------------------------------------------------------------------------
    //  終了処理
    //--------------------------------------------------------------------------------
    void Uninit(EnemyController& actor) override {}

    //--------------------------------------------------------------------------------
    //  更新処理
    //--------------------------------------------------------------------------------
    void Update(EnemyController& actor) override {}

    //--------------------------------------------------------------------------------
    //  後更新処理
    //--------------------------------------------------------------------------------
    void LateUpdate(EnemyController& actor) override {}

    //--------------------------------------------------------------------------------
    //  コライダートリガーの時呼ばれる
    //--------------------------------------------------------------------------------
    void OnTrigger(EnemyController& actor, Collider& self, Collider& other) override {}

    //--------------------------------------------------------------------------------
    //  コライダー衝突の時呼ばれる
    //--------------------------------------------------------------------------------
    void OnCollision(EnemyController& actor, CollisionInfo& info) override {}
};