#include "UISystem.h"
#include "UIObject.h"

//--------------------------------------------------------------------------------
//  クラス
//--------------------------------------------------------------------------------
//--------------------------------------------------------------------------------
//  コンストラクタ
//--------------------------------------------------------------------------------
UIObject::UIObject(const us order)
	: order(order)
{
	UISystem::Instance()->Register(this);
}

//--------------------------------------------------------------------------------
//  リリース
//--------------------------------------------------------------------------------
void UIObject::Release(void)
{
	UISystem::Instance()->Deregister(this);
	Uninit();
	delete this;
}