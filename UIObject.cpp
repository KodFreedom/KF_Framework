#include "main.h"
#include "manager.h"
#include "UISystem.h"
#include "UIObject.h"

//--------------------------------------------------------------------------------
//  クラス
//--------------------------------------------------------------------------------
//--------------------------------------------------------------------------------
//  コンストラクタ
//--------------------------------------------------------------------------------
CUIObject::CUIObject(const unsigned short& usPriority)
	: c_usPriority(usPriority)
{
	GetManager()->GetUISystem()->Register(this);
}

//--------------------------------------------------------------------------------
//  リリース
//--------------------------------------------------------------------------------
void CUIObject::Release(void)
{
	GetManager()->GetUISystem()->Deregister(this);
}