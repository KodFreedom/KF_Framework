#include "main.h"
#include "manager.h"
#include "UISystem.h"
#include "UIObject.h"

//--------------------------------------------------------------------------------
//  クラス
//--------------------------------------------------------------------------------
//--------------------------------------------------------------------------------
//  コンストラクタ
//--------------------------------------------------------------------------------
CUIObject::CUIObject(const unsigned short& usOrder)
	: m_usOrder(usOrder)
{
	Main::GetManager()->GetUISystem()->Register(this);
}

//--------------------------------------------------------------------------------
//  リリース
//--------------------------------------------------------------------------------
void CUIObject::Release(void)
{
	Main::GetManager()->GetUISystem()->Deregister(this);
	Uninit();
	delete this;
}