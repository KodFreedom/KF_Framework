#include "mesh.h"
#include "meshManager.h"

//--------------------------------------------------------------------------------
//
//	Public
//
//--------------------------------------------------------------------------------
//--------------------------------------------------------------------------------
//  終了処理
//--------------------------------------------------------------------------------
void Mesh::Uninit(void)
{
	if (!meshName.empty())
	{
		MeshManager::Instance()->Disuse(meshName);
		meshName.clear();
	}
}

//--------------------------------------------------------------------------------
//  更新処理
//--------------------------------------------------------------------------------
void Mesh::SetMeshName(const string& name)
{
	if (!meshName.empty())
	{
		MeshManager::Instance()->Disuse(meshName);
		meshName.clear();
	}

	meshName = name;
	MeshManager::Instance()->Use(meshName);
}