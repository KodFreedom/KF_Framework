class SphereCollider : public Collider
{
public:
	//--------------------------------------------------------------------------------
	//  関数定義
	//--------------------------------------------------------------------------------
	SphereCollider(GameObject* const owner, const ColliderMode& mode, const float& radius)
		: Collider(owner, Sphere, mode)
		, radius(radius) {}
	~SphereCollider() {}

	//Get関数
	float GetRadius(void) const { return radius; }

private:
	//--------------------------------------------------------------------------------
	//  変数定義
	//--------------------------------------------------------------------------------
	float radius;
};