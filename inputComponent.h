#ifndef _INPUT_COMPONENT_H_
#define _INPUT_COMPONENT_H_

//--------------------------------------------------------------------------------
//  インクルードファイル
//--------------------------------------------------------------------------------
#include "component.h"

//--------------------------------------------------------------------------------
//  前方宣言
//--------------------------------------------------------------------------------
class CGameObject;

//--------------------------------------------------------------------------------
//  クラス宣言
//--------------------------------------------------------------------------------
//--------------------------------------------------------------------------------
//  入力コンポネントクラス
//--------------------------------------------------------------------------------
class CInputComponent : public CComponent
{
public:
	//--------------------------------------------------------------------------------
	//  関数定義
	//--------------------------------------------------------------------------------
	CInputComponent() {}
	~CInputComponent() {}

	virtual KFRESULT	Init(void) = 0;
	virtual void		Uninit(void) = 0;
	virtual void		Update(CGameObject &gameObj) = 0;
	virtual void		Release(void) = 0;
	virtual void		ReceiveMsg(const MESSAGE &msg) override = 0;
};

//--------------------------------------------------------------------------------
//  ヌル入力コンポネントクラス
//--------------------------------------------------------------------------------
class CNullInputComponent : public CInputComponent
{
public:
	//--------------------------------------------------------------------------------
	//  関数定義
	//--------------------------------------------------------------------------------
	CNullInputComponent() {}
	~CNullInputComponent() {}

	KFRESULT	Init(void) override {}
	void		Uninit(void) override {}
	void		Update(CGameObject &gameObj) override {}
	void		Release(void) override {}
	void		ReceiveMsg(const MESSAGE &msg) override {}
};

#endif
