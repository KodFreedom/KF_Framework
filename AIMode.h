class EnemyController;

//--------------------------------------------------------------------------------
//  クラス
//--------------------------------------------------------------------------------
class CAIMode
{
public:
	enum AI_MODE
	{
		ANormal,
		AM_ATTACK
	};

	CAIMode(const AI_MODE& mode) : mode(mode) {}
	virtual ~CAIMode() {}
	virtual void Update(EnemyController& enemy) = 0;
	AI_MODE	GetMode(void) const { return mode; }

private:
	AI_MODE mode;
};