class CEnemyBehaviorComponent;

//--------------------------------------------------------------------------------
//  クラス
//--------------------------------------------------------------------------------
class CAIMode
{
public:
	enum AI_MODE
	{
		AM_NORMAL,
		AM_ATTACK
	};

	CAIMode(const AI_MODE& mode) : m_mode(mode) {}
	virtual ~CAIMode() {}
	virtual void Update(CEnemyBehaviorComponent& enemy) = 0;
	AI_MODE	GetMode(void) const { return m_mode; }

private:
	AI_MODE m_mode;
};