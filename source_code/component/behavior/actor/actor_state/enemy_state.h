#pragma once
#include "actor_state.h"

//--------------------------------------------------------------------------------
//  前方宣言
//--------------------------------------------------------------------------------
class EnemyController;
class Collider;
class CollisionInfo;

//--------------------------------------------------------------------------------
//  エネミーステートの基底クラス
//--------------------------------------------------------------------------------
class EnemyState : public ActorState
{
public:
    //--------------------------------------------------------------------------------
    //  constructors for singleton
    //--------------------------------------------------------------------------------
    EnemyState(const String& name) : ActorState(name) {}
    ~EnemyState() {}

    //--------------------------------------------------------------------------------
    //  初期化処理
    //--------------------------------------------------------------------------------
    virtual void Init(EnemyController& actor) {}

    //--------------------------------------------------------------------------------
    //  終了処理
    //--------------------------------------------------------------------------------
    virtual void Uninit(EnemyController& actor) {}

    //--------------------------------------------------------------------------------
    //  更新処理
    //--------------------------------------------------------------------------------
    virtual void Update(EnemyController& actor) {}

    //--------------------------------------------------------------------------------
    //  後更新処理
    //--------------------------------------------------------------------------------
    virtual void LateUpdate(EnemyController& actor) {}

    //--------------------------------------------------------------------------------
    //  コライダートリガーの時呼ばれる
    //--------------------------------------------------------------------------------
    virtual void OnTrigger(EnemyController& actor, Collider& self, Collider& other) {}

    //--------------------------------------------------------------------------------
    //  コライダー衝突の時呼ばれる
    //--------------------------------------------------------------------------------
    virtual void OnCollision(EnemyController& actor, CollisionInfo& info) {}

    //--------------------------------------------------------------------------------
    //  ダメージ受けた処理
    //--------------------------------------------------------------------------------
    virtual void OnDamaged(EnemyController& actor, const float& damage) {}
};

//--------------------------------------------------------------------------------
//  ヌルエネミーステート
//--------------------------------------------------------------------------------
class NullEnemyState : public EnemyState
{
public:
    //--------------------------------------------------------------------------------
    //  constructors for singleton
    //--------------------------------------------------------------------------------
    NullEnemyState() : EnemyState(L"NullEnemyState") {}
    ~NullEnemyState() {}
};