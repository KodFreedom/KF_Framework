#include "main.h"
#include "mode.h"
#include "camera.h"
#include "manager.h"
#include "gameObjectManager.h"
#include "UISystem.h"

//--------------------------------------------------------------------------------
//  クラス
//--------------------------------------------------------------------------------
//--------------------------------------------------------------------------------
//  コンストラクタ
//--------------------------------------------------------------------------------
CMode::CMode()
{
	static CNullCamera nullCamera;
	m_pCamera = &nullCamera;
}

//--------------------------------------------------------------------------------
//  終了処理
//--------------------------------------------------------------------------------
void CMode::Uninit(void)
{
	//カメラの破棄
	SAFE_RELEASE(m_pCamera);

	//オブジェクトの破棄
	CMain::GetManager()->GetGameObjectManager()->ReleaseAll();

	CMain::GetManager()->GetUISystem()->ReleaseAll();
}

//--------------------------------------------------------------------------------
//  更新処理
//--------------------------------------------------------------------------------
void CMode::Update(void)
{
	//カメラの更新
	m_pCamera->Update();
	
	//オブジェクトの更新
	CMain::GetManager()->GetGameObjectManager()->UpdateAll();
}

//--------------------------------------------------------------------------------
//  更新処理(描画直前)
//--------------------------------------------------------------------------------
void CMode::LateUpdate(void)
{
	//カメラの更新
	m_pCamera->LateUpdate();

	//オブジェクトの更新
	CMain::GetManager()->GetGameObjectManager()->LateUpdateAll();
}

//--------------------------------------------------------------------------------
//  描画処理
//--------------------------------------------------------------------------------
void CMode::Draw(void)
{
	//カメラのセット
	m_pCamera->Set();

	//オブジェクトの描画
	CMain::GetManager()->GetGameObjectManager()->DrawAll();
}