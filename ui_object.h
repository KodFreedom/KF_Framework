#pragma once

//--------------------------------------------------------------------------------
//  UIオブジェクトクラス
//--------------------------------------------------------------------------------
class UIObject
{
public:
	//--------------------------------------------------------------------------------
	//  constructors and destructors
	//--------------------------------------------------------------------------------
	UIObject(const int order = 0);
	~UIObject() {}

	//--------------------------------------------------------------------------------
	//  初期化処理
	//--------------------------------------------------------------------------------
	virtual bool Init(void) = 0;

	//--------------------------------------------------------------------------------
	//  終了処理
	//--------------------------------------------------------------------------------
	virtual void Uninit(void) = 0;

	//--------------------------------------------------------------------------------
	//  更新処理
	//--------------------------------------------------------------------------------
	virtual void Update(void) {}

	//--------------------------------------------------------------------------------
	//  描画処理
	//--------------------------------------------------------------------------------
	virtual void Render(void) = 0;

	//--------------------------------------------------------------------------------
	//  破棄処理
	//--------------------------------------------------------------------------------
	void Release(void);

	//--------------------------------------------------------------------------------
	//  オーダーの取得
	//--------------------------------------------------------------------------------
	int GetOrder(void) const { return order_; }

private:
	//--------------------------------------------------------------------------------
	//  変数定義
	//--------------------------------------------------------------------------------
	int order_;
};