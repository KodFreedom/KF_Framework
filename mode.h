class Mode
{
public:
	//--------------------------------------------------------------------------------
	//  関数宣言
	//--------------------------------------------------------------------------------
	Mode(const string& name) : name(name) {}
	~Mode() {}
	virtual void Init(void) = 0;
	virtual void Update(void) {}
	virtual void LateUpdate(void) {}
	void		 Release(void)
	{
		uninit();
		delete this;
	}

protected:
	//--------------------------------------------------------------------------------
	//  関数宣言
	//--------------------------------------------------------------------------------
	virtual void uninit(void);

	//--------------------------------------------------------------------------------
	//  変数定義
	//--------------------------------------------------------------------------------
	const string name;
};