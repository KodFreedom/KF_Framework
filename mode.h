#pragma once
#include "common_setting.h"

//--------------------------------------------------------------------------------
//  クラス宣言
//--------------------------------------------------------------------------------
class Mode
{
public:
    //--------------------------------------------------------------------------------
    //  関数宣言
    //--------------------------------------------------------------------------------
    Mode(const String& name) : name_(name) {}
    ~Mode() {}

    virtual void Init(void) = 0;
    virtual void Update(void) {}
    virtual void LateUpdate(void) {}
    virtual void OnCompleteLoading(void) {}
    void         Release(void)
    {
        Uninit();
        MY_DELETE this;
    }

protected:
    //--------------------------------------------------------------------------------
    //  関数宣言
    //--------------------------------------------------------------------------------
    virtual void Uninit(void);

    //--------------------------------------------------------------------------------
    //  変数定義
    //--------------------------------------------------------------------------------
    const String name_;
};