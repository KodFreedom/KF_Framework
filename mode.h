class CCamera;

//--------------------------------------------------------------------------------
//  クラス宣言
//--------------------------------------------------------------------------------
class CMode
{
public:
	//--------------------------------------------------------------------------------
	//  関数宣言
	//--------------------------------------------------------------------------------
	CMode();
	~CMode() {}

	virtual void	Init(void) = 0;
	virtual void	Update(void);
	virtual void	LateUpdate(void);
	virtual void	EndMode(void) = 0;
	void			CameraSet(void);
	void			Release(void)
	{
		uninit();
		delete this;
	}

	//Get関数
	CCamera*		GetCamera(void) { return m_pCamera; }

protected:
	//--------------------------------------------------------------------------------
	//  関数宣言
	//--------------------------------------------------------------------------------
	virtual void	uninit(void);

	//--------------------------------------------------------------------------------
	//  変数宣言
	//--------------------------------------------------------------------------------
	CCamera*		m_pCamera;
};