//--------------------------------------------------------------------------------
//  クラス宣言
//--------------------------------------------------------------------------------
//--------------------------------------------------------------------------------
//  メッシュコンポネントクラス
//--------------------------------------------------------------------------------
class MeshComponent : public CComponent
{
public:
	//--------------------------------------------------------------------------------
	//  関数定義
	//--------------------------------------------------------------------------------
	MeshComponent(CGameObject* const pGameObj)
		: CComponent(pGameObj) 
	{
		m_strMeshName.clear();
	}
	~MeshComponent() {}

	virtual bool	Init(void) override { return true; }
	virtual void	Uninit(void) override;
	virtual void	Update(void) {}

	//Set関数
	void			SetMeshName(const string& strMeshName);

	//Get関数
	const string&	GetMeshName(void) const { return m_strMeshName; }

protected:
	//--------------------------------------------------------------------------------
	//  関数定義
	//--------------------------------------------------------------------------------
	MeshComponent() : CComponent() {}

	//--------------------------------------------------------------------------------
	//  変数定義
	//--------------------------------------------------------------------------------
	string	m_strMeshName;	//メッシュの名前
};

//--------------------------------------------------------------------------------
//  ヌルメッシュコンポネントクラス
//--------------------------------------------------------------------------------
class CNullMeshComponent : public MeshComponent
{
public:
	//--------------------------------------------------------------------------------
	//  関数定義
	//--------------------------------------------------------------------------------
	CNullMeshComponent() : MeshComponent() {}
	~CNullMeshComponent() {}

	bool	Init(void) override { return true; }
	void	Uninit(void) override {}
	void	Update(void) override {}
	void	Release(void) override {}
};